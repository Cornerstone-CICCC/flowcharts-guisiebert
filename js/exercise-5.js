const studentName = "John"
const studentGrade = 85

if (studentGrade >= 80) {
    console.log(`${studentName} got grade A`)
} else if (studentGrade >= 60) {
    console.log(`${studentName} got grade B`)
} else if (studentGrade >= 40) {
    console.log(`${studentName} got grade C`)
} else {
    console.log(`${studentName} got no grade`)
}